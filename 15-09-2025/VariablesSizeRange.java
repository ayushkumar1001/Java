public class VariablesSizeRange {
    public static void main (String arg[]) {
        byte b =127;
    
        System.out.println(b);

        short s= 300;

        System.out.println(s);

        int i = 100;

       System.out.println(i);

       Float f= 25.3f;

       System.out.println(f);

       char c= 'A';

       System.out.println(c);
    }
    
}
